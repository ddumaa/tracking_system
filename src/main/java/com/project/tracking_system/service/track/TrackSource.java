package com.project.tracking_system.service.track;

/**
 * Источник появления трека в системе.
 * <p>
 * Используется для классификации треков, добавленных вручную,
 * импортированных из Excel или появившихся при автообновлении.
 * </p>
 */
public enum TrackSource {
    /** Трек введён пользователем вручную. */
    MANUAL,
    /** Трек импортирован из Excel-файла. */
    EXCEL,
    /** Трек добавлен в процессе автоматического обновления. */
    AUTO,
    /** Трек поставлен в очередь через ручное обновление. */
    UPDATE
}