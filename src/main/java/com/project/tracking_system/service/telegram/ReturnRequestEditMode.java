package com.project.tracking_system.service.telegram;

/**
 * Режим редактирования активной заявки на возврат или обмен.
 * <p>
 * Используется ботом для понимания того, какие данные ожидаются
 * от пользователя: только трек-номер обратной отправки либо
 * свободный комментарий.
 * </p>
 */
public enum ReturnRequestEditMode {
    /**
     * Бот ожидает ввод трек-номера обратной отправки.
     */
    TRACK,

    /**
     * Бот ожидает текстовый комментарий к заявке.
     */
    COMMENT
}
