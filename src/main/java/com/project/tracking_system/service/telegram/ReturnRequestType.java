package com.project.tracking_system.service.telegram;

/**
 * Тип заявки, которую оформляет покупатель в диалоге с Telegram-ботом.
 * <p>
 * Используется для определения ветки сценария и корректного текста подсказок
 * при выборе магазина и посылки.
 * </p>
 */
public enum ReturnRequestType {

    /**
     * Оформление возврата товара.
     */
    RETURN,

    /**
     * Оформление обмена товара.
     */
    EXCHANGE
}

