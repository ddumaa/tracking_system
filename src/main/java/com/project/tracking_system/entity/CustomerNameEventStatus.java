package com.project.tracking_system.entity;

/**
 * Статус заявки на подтверждение ФИО.
 * <p>
 * Новая заявка создаётся в состоянии {@link #PENDING} и ожидает
 * ответа покупателя. Подтверждённые или изменённые данные
 * отмечаются как {@link #APPLIED}, отклонённые — {@link #REJECTED}.
 * Просроченные без ответа заявки переходят в {@link #EXPIRED},
 * а заменённые новой — в {@link #SUPERSEDED}.
 * </p>
 */
public enum CustomerNameEventStatus {
    /** Заявка ожидает ответа покупателя. */
    PENDING,
    /** Заявка заменена более новой. */
    SUPERSEDED,
    /** Покупатель подтвердил или изменил ФИО. */
    APPLIED,
    /** Покупатель отклонил предложение. */
    REJECTED,
    /** Заявка просрочена без ответа. */
    EXPIRED
}
