package com.project.tracking_system.entity;

/**
 * Состояния жизненного цикла эпизода заказа, включающие открытое и финальные исходы.
 * <p>
 * Значения отражают ключевые сценарии обработки обращения:
 * эпизод может оставаться открытым, успешно завершиться с/без обмена,
 * закрыться возвратом без замены либо быть отменённым.
 * </p>
 */
public enum OrderFinalOutcome {

    /** Эпизод остаётся открытым, финальное решение ещё не принято. */
    OPEN,

    /** Посылка успешно вручена без оформления обмена. */
    SUCCESS_NO_EXCHANGE,

    /** Посылка вручена после как минимум одного обмена. */
    SUCCESS_AFTER_EXCHANGE,

    /** Посылка возвращена, покупатель отказался от обмена. */
    RETURNED_NO_REPLACEMENT,

    /** Регистрация отправления отменена до завершения доставки. */
    CANCELLED
}
