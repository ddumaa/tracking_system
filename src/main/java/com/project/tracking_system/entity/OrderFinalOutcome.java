package com.project.tracking_system.entity;

/**
 * Финальные исходы эпизода заказа.
 * <p>
 * Используются для фиксации завершившегося жизненного цикла посылок,
 * объединённых в один эпизод заказа.
 * </p>
 */
public enum OrderFinalOutcome {

    /** Эпизод завершился успешной выдачей посылки. */
    DELIVERED,

    /** Посылка была возвращена отправителю. */
    RETURNED,

    /** Регистрация отправления отменена до доставки. */
    CANCELLED,

    /** Эпизод закрыт после обмена на другую посылку. */
    EXCHANGED
}
