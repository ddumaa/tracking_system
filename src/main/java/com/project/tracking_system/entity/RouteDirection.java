package com.project.tracking_system.entity;

/**
 * Направление перемещения посылки в процессе доставки.
 * <p>
 * Значения перечисления помогают однозначно определить, доставляется ли посылка
 * покупателю или находится на обратном пути в магазин/на склад.
 * </p>
 */
public enum RouteDirection {
    /**
     * Посылка движется в сторону покупателя.
     */
    TO_CUSTOMER,

    /**
     * Посылка возвращается в магазин или на склад.
     */
    RETURN_TO_SENDER
}

