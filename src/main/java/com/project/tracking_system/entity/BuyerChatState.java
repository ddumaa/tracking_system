package com.project.tracking_system.entity;

/**
 * Сценарные состояния диалога покупателя с Telegram-ботом.
 * <p>
 * Используются для восстановления контекста между сообщениями и после рестарта приложения.
 * </p>
 */
public enum BuyerChatState {

    /**
     * Бот ожидает любую команду пользователя из главного меню.
     */
    IDLE,

    /**
     * Бот ждёт от пользователя контакт с номером телефона.
     */
    AWAITING_CONTACT,

    /**
     * Бот ожидает текстовый ввод полного имени пользователя.
     */
    AWAITING_NAME_INPUT,

    /**
     * Бот ожидает выбор типа заявки (возврат или обмен).
     */
    AWAITING_REQUEST_TYPE,

    /**
     * Бот ожидает выбор магазина, в котором была получена посылка.
     */
    AWAITING_STORE_SELECTION,

    /**
     * Бот ожидает выбор конкретной посылки из выбранного магазина.
     */
    AWAITING_PARCEL_SELECTION,

    /**
     * Бот ожидает выбор действия по активной заявке (запуск обмена, закрытие).
     */
    AWAITING_REQUEST_ACTION,

    /**
     * Бот собирает причину оформления возврата для выбранной посылки.
     */
    AWAITING_RETURN_REASON,

    /**
     * Бот ожидает дополнительный комментарий к заявке на возврат.
     */
    AWAITING_RETURN_COMMENT,

    /**
     * Бот ожидает дату, когда покупатель решил оформить возврат.
     */
    AWAITING_RETURN_DATE,

    /**
     * Бот ожидает трек-номер обратной отправки, если он уже известен.
     */
    AWAITING_RETURN_TRACK,

    /**
     * Бот ожидает подтверждения запуска обмена по ранее созданной заявке.
     */
    AWAITING_EXCHANGE_CONFIRM
}
